package com.example.topsports;

public class SportCount {
    private String idSport;
    private String numeSport;
    private String nrSport;

    public String getIdSport() {
        return idSport;
    }

    public void setIdSport(String idSport) {
        this.idSport = idSport;
    }

    public String getNumeSport() {
        return numeSport;
    }

    public void setNumeSport(String numeSport) {
        this.numeSport = numeSport;
    }

    public String getNrSport() {
        return nrSport;
    }

    public void setNrSport(String nrSport) {
        this.nrSport = nrSport;
    }
}

